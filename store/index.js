export const getters = {
	isAuthenticated (state) {
		return state.auth.loggedIn;
	},

	loggedInUser (state) {
		return state.auth.user;
	},

	async getToken () {
		if (process.client) {
			return localStorage.getItem('auth._token.local');
		}
	}
};
